package obj01;

public class Mathematics {
	double a,b,c;
	double ceil(double a){
		b=Math.ceil(a);
		return b;	
	}
	double floor(double a){
		b=Math.floor(a);
		return b;	
	}
	double rint(double a){
		b=Math.rint(a);
		return b;	
	}
	double pow(double a,int b){
		c=Math.pow(a,b);
		return c;	
	}
	double abs(double a){
		b=Math.abs(a);
		return b;	
	}
	double max(double a,double b){
		c=Math.max(a,b);
		return c;	
	}
	double min(double a,double b){
		c=Math.min(a,b);
		return c;	
	}
}
